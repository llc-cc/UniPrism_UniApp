export function getProfessionalPage(pageId) {
  return MATH_PROFESSIONAL_PAGES[pageId] || null
}

export function listProfessionalPageIds() {
  return Object.keys(MATH_PROFESSIONAL_PAGES)
}

/**
 * 可选：从 Web API 拉取单页内容；失败时回退本地配置。
 * @param {string} pageId
 */
export async function loadProfessionalPage(pageId) {
  try {
    const response = await api.fetchMathProfessionalPage(pageId)
    const page = response?.data
    if (page?.pageId && Array.isArray(page.sections) && page.sections.length) {
      return page
    }
  } catch (error) {
    console.warn('[math-professional-pages] fetch failed, using fallback', error)
  }
  return getProfessionalPage(pageId)
}
