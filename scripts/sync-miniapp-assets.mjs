/**
 * 从 mini-program-asset-urls.md 校验远程资源，下载可用文件到 static/assets/remote/，
 * 并生成 business/remote-asset-registry.js 供 resolveAsset 使用。
 *
 * 用法: node scripts/sync-miniapp-assets.mjs
 * 可选: ASSET_HOST=https://uniprism.cn node scripts/sync-miniapp-assets.mjs
 */
import fs from 'fs'
import path from 'path'
import https from 'https'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')
const mdPath = path.resolve(root, '..', 'mini-program-asset-urls.md')
const outRoot = path.join(root, 'static', 'assets', 'remote')
const registryPath = path.join(root, 'business', 'remote-asset-registry.js')

const HOST = (process.env.ASSET_HOST || 'https://uniprism.cn').replace(/\/+$/, '')
const hostname = new URL(HOST).hostname
const CONCURRENCY = Number(process.env.ASSET_SYNC_CONCURRENCY || 8)
const REQUEST_TIMEOUT = Number(process.env.ASSET_SYNC_TIMEOUT || 30000)

function parsePathsFromMd(content) {
  const re = /https:\/\/uniprism\.cn(\/(?:design|images|videos)\/[^\s)]+)/g
  const seen = new Set()
  const paths = []
  for (const match of content.matchAll(re)) {
    const objectPath = match[1]
    if (!seen.has(objectPath)) {
      seen.add(objectPath)
      paths.push(objectPath)
    }
  }
  return paths
}

function httpsRequest(options) {
  return new Promise((resolve, reject) => {
    const req = https.request({ ...options, timeout: REQUEST_TIMEOUT }, (res) => {
      resolve(res)
    })
    req.on('error', reject)
    req.on('timeout', () => {
      req.destroy()
      reject(new Error('timeout'))
    })
    req.end()
  })
}

async function probeObjectPath(objectPath) {
  const candidates = [objectPath, `/api/oss-assets${objectPath}`]
  for (const requestPath of candidates) {
    try {
      const res = await httpsRequest({
        hostname,
        path: requestPath,
        method: 'HEAD',
        headers: { Host: hostname },
      })
      res.resume()
      if (res.statusCode >= 200 && res.statusCode < 300) {
        return { ok: true, requestPath, status: res.statusCode }
      }
    } catch {
      // try next candidate
    }
  }
  return { ok: false, requestPath: null, status: 0 }
}

function downloadObject(objectPath, requestPath) {
  return new Promise((resolve, reject) => {
    const filePath = path.join(outRoot, objectPath.replace(/^\//, ''))
    fs.mkdirSync(path.dirname(filePath), { recursive: true })

    const req = https.request(
      {
        hostname,
        path: requestPath,
        method: 'GET',
        headers: { Host: hostname },
        timeout: REQUEST_TIMEOUT,
      },
      (res) => {
        if (res.statusCode !== 200) {
          res.resume()
          reject(new Error(`HTTP ${res.statusCode}`))
          return
        }
        const chunks = []
        res.on('data', (chunk) => chunks.push(chunk))
        res.on('end', () => {
          fs.writeFileSync(filePath, Buffer.concat(chunks))
          resolve(filePath)
        })
      },
    )
    req.on('error', reject)
    req.on('timeout', () => {
      req.destroy()
      reject(new Error('timeout'))
    })
    req.end()
  })
}

async function mapWithConcurrency(items, limit, worker) {
  const results = new Array(items.length)
  let index = 0

  async function runWorker() {
    while (index < items.length) {
      const current = index
      index += 1
      results[current] = await worker(items[current], current)
    }
  }

  await Promise.all(Array.from({ length: Math.min(limit, items.length) }, runWorker))
  return results
}

function writeRegistry({ verifiedPaths, localMap, stats }) {
  const header = `/**
 * AUTO-GENERATED by scripts/sync-miniapp-assets.mjs — do not edit manually.
 * Source: mini-program-asset-urls.md
 * Generated: ${new Date().toISOString()}
 * Verified: ${stats.verified}/${stats.total}, downloaded: ${stats.downloaded}, failed: ${stats.failed}
 */
`
  const body = `${header}
export const VERIFIED_REMOTE_ASSET_PATHS = new Set(${JSON.stringify(verifiedPaths, null, 2)})

export const REMOTE_ASSET_LOCAL_MAP = ${JSON.stringify(localMap, null, 2)}

export function isVerifiedRemoteAsset(path) {
  return VERIFIED_REMOTE_ASSET_PATHS.has(path)
}

export function getDownloadedRemoteAsset(path) {
  return REMOTE_ASSET_LOCAL_MAP[path] || null
}
`
  fs.writeFileSync(registryPath, body, 'utf8')
}

async function main() {
  if (!fs.existsSync(mdPath)) {
    console.error(`Missing ${mdPath}`)
    process.exit(1)
  }

  const md = fs.readFileSync(mdPath, 'utf8')
  const objectPaths = parsePathsFromMd(md)
  console.log(`Found ${objectPaths.length} asset paths in MD`)

  // refresh MD catalog (no network)
  const catalogOut = path.join(root, 'business', 'md-asset-catalog.js')
  fs.writeFileSync(
    catalogOut,
    `/** AUTO-GENERATED — see scripts/sync-miniapp-assets.mjs */\nexport const MD_ASSET_PATHS = new Set(${JSON.stringify(objectPaths, null, 2)})\n\nexport function isMdCatalogAsset(path) {\n  return MD_ASSET_PATHS.has(path)\n}\n`,
    'utf8',
  )

  const verifiedPaths = []
  const localMap = {}
  let downloaded = 0
  let failed = 0

  await mapWithConcurrency(objectPaths, CONCURRENCY, async (objectPath) => {
    const probe = await probeObjectPath(objectPath)
    if (!probe.ok) {
      failed += 1
      console.error(`SKIP ${objectPath}`)
      return
    }

    try {
      await downloadObject(objectPath, probe.requestPath)
      const localPath = `/static/assets/remote/${objectPath.replace(/^\//, '')}`
      verifiedPaths.push(objectPath)
      localMap[objectPath] = localPath
      downloaded += 1
      process.stdout.write('.')
    } catch (error) {
      failed += 1
      console.error(`\nFAIL download ${objectPath}: ${error.message}`)
    }
  })

  verifiedPaths.sort()
  writeRegistry({
    verifiedPaths,
    localMap,
    stats: {
      total: objectPaths.length,
      verified: verifiedPaths.length,
      downloaded,
      failed,
    },
  })

  console.log(`\nDone. verified=${verifiedPaths.length} downloaded=${downloaded} failed=${failed}`)
  console.log(`Registry: ${registryPath}`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
